package collection_Practice_program;

public class Practice07 {

	// To find the factorial of any number
	
	public static void main(String[] args) 
	{
		// To find the factorial of any number
		long a,b,c=1;
		b=5;				//factorial of 5
		for (a=1; a<=b; a++)
		{
			c=c*a;
		}
		System.out.println(c);

	}

}
