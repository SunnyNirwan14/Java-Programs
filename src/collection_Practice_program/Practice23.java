public class Practice23 
{
	public static void main(String[] args) 
	{
			//Star Pattern - Square
		/*
		 * 		**********
		 * 		**********
		 * 		**********
		 * 		**********
		 * 		**********
		 */
		
		int a;
		int b;
		
		for (a=1; a<=5; a++)
		{
			for (b=1; b<=10; b++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}
